    use super::*;

    #[test]
    fn rs_zeros() {
        let data = [0u8; 9];
        let parity = rs_12_9_encode(&data);
        assert_eq!(parity, [0, 0, 0]);
    }

    #[test]
    fn rs_known_lc() {
        // Voice LC payload bytes 0..9 for group call to TG 91 from
        // src 3151238.  The RS(12,9) parity bytes that follow are
        // what a BPTC-encoded voice LC needs to match the real
        // Brandmeister capture tested in dmr::bptc::tests.
        let data: [u8; 9] = [0x00, 0x00, 0x00, 0x00, 0x00, 0x5B, 0x30, 0x15, 0x86];
        assert_eq!(rs_12_9_encode(&data), [0xE3, 0x9C, 0xBE]);
        // rs_12_9_fec reverses and XORs with the per-data-type mask;
        // for the header mask [0x96; 3] that gives bytes 9..12 of LC.
        assert_eq!(rs_12_9_fec(&data, &LC_HEADER_MASK), [0x28, 0x0A, 0x75]);
    }

    #[test]
    fn rs_generator_matrix_unit_vectors() {
        // ETSI TS 102 361-1 Table B.18: RS(12,9) generator matrix.
        // Row i of G is the codeword for message m = e_i (unit vector
        // with m_8 at index 0 per ETSI's (m_{K-1}, ..., m_0) convention).
        // Columns 9..12 of each row are the parity bytes (p_2, p_1, p_0).
        // rs_12_9_encode returns [p_0, p_1, p_2], so compare reversed.
        //
        // These nine vectors fully characterize a linear RS(12,9)
        // encoder: any message is a GF(256) linear combination of the
        // unit vectors, and if all nine match spec, the encoder matches
        // spec for every input.
        const GENERATOR_PARITY_P2_P1_P0: [[u8; 3]; 9] = [
            [0x1C, 0xBC, 0xFD], // row 0: m_8 = 1
            [0x89, 0x31, 0x08], // row 1: m_7 = 1
            [0xAD, 0x41, 0x36], // row 2: m_6 = 1
            [0x7D, 0x71, 0x16], // row 3: m_5 = 1
            [0xF3, 0xA6, 0x3A], // row 4: m_4 = 1
            [0x08, 0x83, 0x7B], // row 5: m_3 = 1
            [0x3F, 0x6F, 0x02], // row 6: m_2 = 1
            [0x6C, 0x0D, 0xA7], // row 7: m_1 = 1
            [0x0E, 0x38, 0x40], // row 8: m_0 = 1
        ];
        for (row, [p2, p1, p0]) in GENERATOR_PARITY_P2_P1_P0.iter().enumerate() {
            let mut m = [0u8; 9];
            m[row] = 1;
            let expected = [*p0, *p1, *p2];
            assert_eq!(rs_12_9_encode(&m), expected, "row {row} of generator");
        }
    }

    /// Build the 12-byte codeword (m, p_2, p_1, p_0) per ETSI from
    /// 9 data bytes.
    fn build_codeword(data: &[u8; 9]) -> [u8; 12] {
        let parity = rs_12_9_encode(data);
        let mut cw = [0u8; 12];
        cw[..9].copy_from_slice(data);
        cw[9] = parity[2]; // p_2
        cw[10] = parity[1]; // p_1
        cw[11] = parity[0]; // p_0
        cw
    }

