    #[test]
    fn rs_correct_detects_two_byte_error() {
        // Flip two different bytes.  The consistency check
        // S1*S3 != S2^2 should fail for most combinations.
        let data = [0xAA, 0xBB, 0xCC, 0xDD, 0xEE, 0x11, 0x22, 0x33, 0x44];
        let base = build_codeword(&data);
        let mut detected = 0usize;
        for i in 0..12 {
            for j in (i + 1)..12 {
                let mut cw = base;
                cw[i] ^= 0x01;
                cw[j] ^= 0x01;
                if !rs_12_9_correct(&mut cw) {
                    detected += 1;
                }
            }
        }
        // RS(12,9,4) distance 4 guarantees detection of all
        // patterns with <= d-1 = 3 symbol errors.  66 = C(12,2)
        // two-byte error patterns, all must be detected.
        assert_eq!(detected, 66, "expected all 2-byte errors detected");
    }

