//! Reed-Solomon RS(12,9) encoder over GF(256).
//!
//! Used for Full LC FEC in voice headers and terminators.
//! 9 data bytes -> 3 parity bytes.
//!
//! Tables cross-checked against DMRGateway RS129.cpp.

/// RS(12,9) parity count.
const NPAR: usize = 3;

/// Generator polynomial coefficients.
const POLY: [u8; 12] = [64, 56, 14, 1, 0, 0, 0, 0, 0, 0, 0, 0];

/// GF(256) multiplication via log/exp tables.
fn gmult(a: u8, b: u8) -> u8 {
    if a == 0 || b == 0 {
        return 0;
    }
    let log_a = LOG_TABLE[a as usize] as usize;
    let log_b = LOG_TABLE[b as usize] as usize;
    EXP_TABLE[log_a + log_b]
}

/// GF(256) division: a / b.  Panics if b == 0.
fn gdiv(a: u8, b: u8) -> u8 {
    if a == 0 {
        return 0;
    }
    debug_assert!(b != 0, "GF(256) division by zero");
    let log_a = LOG_TABLE[a as usize] as usize;
    let log_b = LOG_TABLE[b as usize] as usize;
    // log_a - log_b mod 255, using + 255 to avoid underflow.
    EXP_TABLE[log_a + 255 - log_b]
}

/// Encode 9 data bytes, returning 3 parity bytes.
pub(crate) fn rs_12_9_encode(data: &[u8; 9]) -> [u8; NPAR] {
    let mut parity = [0u8; NPAR];
    for &byte in data {
        let dbyte = byte ^ parity[NPAR - 1];
        for j in (1..NPAR).rev() {
            parity[j] = parity[j - 1] ^ gmult(POLY[j], dbyte);
        }
        parity[0] = gmult(POLY[0], dbyte);
    }
    parity
}

/// LC FEC masks per data type (ETSI TS 102 361-1).
pub(crate) const LC_HEADER_MASK: [u8; 3] = [0x96, 0x96, 0x96];
pub(crate) const LC_TERMINATOR_MASK: [u8; 3] = [0x99, 0x99, 0x99];

/// Encode RS(12,9) and apply the per-data-type mask.
/// Returns bytes [9], [10], [11] of the LC payload.
pub(crate) fn rs_12_9_fec(data: &[u8; 9], mask: &[u8; 3]) -> [u8; 3] {
    let parity = rs_12_9_encode(data);
    // DMRGateway stores parity reversed with mask XOR.
    [
        parity[2] ^ mask[0],
        parity[1] ^ mask[1],
        parity[0] ^ mask[2],
    ]
}

/// Compute the three RS(12,9) syndromes of a 12-byte codeword.  A
/// valid codeword (no errors) gives all-zero syndromes.
///
/// Used at runtime by the voice-LC validator in dmr::bptc as a
/// log-only diagnostic check on inbound headers / terminators, and
/// in tests to verify our encoder produces valid codewords.
///
/// Codeword layout per ETSI TS 102 361-1 formula B.13:
///   c = (m_8, m_7, ..., m_0, p_2, p_1, p_0)
/// where c[0] is the highest-degree coefficient.  Syndromes are
/// S_i = c(alpha^i) for i = 1, 2, 3 (the roots of the generator
/// polynomial g(x) = (x+alpha)(x+alpha^2)(x+alpha^3)).
pub(crate) fn rs_12_9_syndromes(codeword: &[u8; 12]) -> [u8; 3] {
    let mut syndromes = [0u8; 3];
    for (i, s) in syndromes.iter_mut().enumerate() {
        let x = EXP_TABLE[i + 1];
        let mut acc: u8 = 0;
        for &c in codeword {
            acc = gmult(acc, x) ^ c;
        }
        *s = acc;
    }
    syndromes
}

/// Codeword length for the RS(12,9) code.
const CODEWORD_LEN: usize = 12;

/// Attempt single-byte error correction on a 12-byte RS(12,9,4)
/// codeword.  Returns true if the codeword was valid or was
/// corrected in place; false if the error is uncorrectable (>= 2
/// byte errors, or single-byte error at an impossible position).
///
/// Algorithm (Peterson-Gorenstein-Zierler for t=1):
///   Given syndromes S1 = e*alpha^p, S2 = e*alpha^(2p), S3 = e*alpha^(3p)
///   where e is the error magnitude and p = 11 - byte_index:
///   1. Consistency: S1*S3 == S2^2 (else >= 2 errors)
///   2. Locator: alpha^p = S2/S1 => p = log(S2/S1)
///   3. Must have 0 <= p <= 11 (valid codeword position)
///   4. Magnitude: e = S1 / alpha^p
///   5. Correct: codeword[11-p] ^= e
pub(crate) fn rs_12_9_correct(codeword: &mut [u8; CODEWORD_LEN]) -> bool {
    let [s1, s2, s3] = rs_12_9_syndromes(codeword);
    if s1 == 0 && s2 == 0 && s3 == 0 {
        return true; // already valid
    }
    if s1 == 0 || s2 == 0 {
        // One syndrome zero + others non-zero => multi-byte error.
        return false;
    }
    // Consistency check: S1*S3 must equal S2^2 for a single-byte
    // error.  In GF(256), squaring is just gmult(x, x).
    if gmult(s1, s3) != gmult(s2, s2) {
        return false;
    }
    // Error locator power: alpha^p = S2/S1.
    let alpha_p = gdiv(s2, s1);
    let p = LOG_TABLE[alpha_p as usize] as usize;
    if p >= CODEWORD_LEN {
        // Error position outside the codeword -- pattern doesn't
        // correspond to a single byte error in our 12-byte word.
        return false;
    }
    // Error magnitude.
    let e = gdiv(s1, alpha_p);
    codeword[CODEWORD_LEN - 1 - p] ^= e;
    true
}

